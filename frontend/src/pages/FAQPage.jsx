const FAQPage = () => {
  const { t } = useAppSettings();
  const [search, setSearch] = useState('');
  const [expanded, setExpanded] = useState(false);

  const handleChange = (panel) => (_, isExpanded) => {
    setExpanded(isExpanded ? panel : false);
  };

  const filtered = search.trim()
    ? FAQ_DATA.map(cat => ({
        ...cat,
        items: cat.items.filter(item =>
          item.q.includes(search) || item.a.includes(search)
        )
      })).filter(cat => cat.items.length > 0)
    : FAQ_DATA;

  return (
    <Container maxWidth="md" sx={{ py: 4 }}>
      {/* Header */}
      <Box sx={{ textAlign: 'center', mb: 4 }}>
        <HelpOutlineIcon sx={{ fontSize: 56, color: 'primary.main', mb: 1 }} />
        <Typography variant="h4" fontWeight={700} gutterBottom>
          الأسئلة الشائعة والدعم
        </Typography>
        <Typography variant="body1" color="text.secondary">
          ستجد هنا إجابات لأكثر الأسئلة شيوعاً حول استخدام المنصة والمشاريع وبرمجة Arduino
        </Typography>
      </Box>

      {/* Search */}
      <Paper sx={{ mb: 4, p: 1 }}>
        <TextField
          fullWidth
          placeholder="ابحث عن سؤال..."
          value={search}
          onChange={e => setSearch(e.target.value)}
          variant="outlined"
          size="small"
          InputProps={{
            startAdornment: (
              <InputAdornment position="start">
                <SearchIcon color="action" />
              </InputAdornment>
            )
          }}
        />
      </Paper>

      {/* FAQ Sections */}
      {filtered.length === 0 ? (
        <Typography color="text.secondary" sx={{ textAlign: 'center', mt: 4 }}>
          لا توجد نتائج للبحث عن "{search}"
        </Typography>
      ) : (
        filtered.map((category, catIdx) => (
          <Box key={catIdx} sx={{ mb: 3 }}>
            <Box sx={{ display: 'flex', alignItems: 'center', gap: 1, mb: 1.5 }}>
              <Chip label={category.category} color="primary" size="small" />
            </Box>
            {category.items.map((item, itemIdx) => {
              const panelId = `panel-${catIdx}-${itemIdx}`;
              return (
                <Accordion
                  key={itemIdx}
                  expanded={expanded === panelId}
                  onChange={handleChange(panelId)}
                  sx={{ mb: 1, '&:before': { display: 'none' }, borderRadius: 1, overflow: 'hidden' }}
                >
                  <AccordionSummary expandIcon={<ExpandMoreIcon />}>
                    <Typography fontWeight={600}>{item.q}</Typography>
                  </AccordionSummary>
                  <AccordionDetails>
                    <Typography variant="body2" color="text.secondary" sx={{ whiteSpace: 'pre-line', lineHeight: 2 }}>
                      {item.a}
                    </Typography>
                  </AccordionDetails>
                </Accordion>
              );
            })}
          </Box>
        ))
      )}

      {/* Contact section */}
      <Box sx={{ mt: 5, p: 3, bgcolor: 'primary.light', borderRadius: 2, textAlign: 'center' }}>
        <Typography variant="h6" fontWeight={600} color="primary.contrastText" gutterBottom>
          هل لديك سؤال آخر؟
        </Typography>
        <Typography variant="body2" color="primary.contrastText">
          يمكنك التواصل مع المعلم المسؤول عبر نظام المحادثة في المنصة، أو استخدام المساعد الذكي للمساعدة الفورية في البرمجة والمشاريع.
        </Typography>
      </Box>
    </Container>
  );
};

export default FAQPage;
